class Calculadora { // clases: me ayudan en la sintaxis, herencia orientada a objetos. Tambien es una function    
                    //metodo: propiedad de un objetoExisten dos tipos de métodos: Métodos de Instancia los cuales son tareas integradas realizadas por la instacia de un objeto, y los Métodos Estáticos que son tareas que pueden ser llamadas directamente en el constructor de un objeto. 
    sumar(num1,num2) {
        return num1+num2;
    }
    restar(num1,num2) {
        return num1-num2;
    }
    multiplicar(num1,num2){
        return num1*num2;
    }
    dividir(num1,num2){
        return num1/num2;
    }
}